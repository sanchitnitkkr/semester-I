# Data types in Python

# String
name = "Sanchit Tewari"
print("type of",name, "is", type(name)) # type of Sanchit Tewari is <class 'str'>

# Integer
age = 23
print("type of",age, "is", type(age)) # type of 23 is <class 'int'>

# Float
height = 167.5
print("type of",height, "is", type(height)) # type of 167.5 is <class 'float'>

# Boolean
playsPiano = False 
print("type of whether plays piano or not is", type(playsPiano))  # type of whether plays piano or not is <class 'bool'>

# Complex Numbers
imaginaryNumber = 1j

# List (Ordered, Mutable, Not Unique, Indexed)
todoItems = ["Complete Assignment", "Take Nap"]

# Tuple (Ordered, Immutable, Not Unique, Indexed)
tupleItems = ("apple", "banana", "cherry")

# Range
rangeNumbers = range(5)

# Dict (Key value pair store)
abbreviations = {"M": "Male", "F": "Female"}

# Set (Unordered, Immutable, Unique, Not Indexed)
uniqueNames = {"Ankit", "Sanchit", "Vijay"}



