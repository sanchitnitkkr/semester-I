DROP TABLE IF EXISTS SP;
DROP TABLE IF EXISTS P;
DROP TABLE IF EXISTS S;

CREATE TABLE S (
    Sno   VARCHAR(5) PRIMARY KEY,
    Sname VARCHAR(20),
    City  VARCHAR(20)
);

CREATE TABLE P (
    Pno   VARCHAR(5) PRIMARY KEY,
    Pname VARCHAR(20),
    Color VARCHAR(10)
);

CREATE TABLE SP (
    Sno VARCHAR(5),
    Pno VARCHAR(5),
    Qty INT,
    PRIMARY KEY (Sno, Pno),
    FOREIGN KEY (Sno) REFERENCES S(Sno),
    FOREIGN KEY (Pno) REFERENCES P(Pno)
);

