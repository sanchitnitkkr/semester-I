SELECT SP.Qty
FROM SP
JOIN P ON SP.Pno = P.Pno
WHERE P.Color = 'Red';

SELECT DISTINCT S.Sname
FROM S
JOIN SP ON S.Sno = SP.Sno
JOIN P ON SP.Pno = P.Pno
WHERE P.Color = 'Red';

SELECT DISTINCT P.Pno, S.City
FROM P
JOIN SP ON P.Pno = SP.Pno
JOIN S ON SP.Sno = S.Sno
ORDER BY P.Pno;

SELECT A.Sno, B.Sno, A.City
FROM S A, S B
WHERE A.City = B.City
AND A.Sno < B.Sno;

